import java.util.*;


public class Huffman {
    public static void main(String[] args) {
    	int arr[] = {1, 6, 3, 4, 14};

    	for(int i = 0; i<arr.length; i++){
    		System.out.print(arr[i]+"|");
    	}
    	System.out.println();

    	System.out.println("Hello");
    }

}